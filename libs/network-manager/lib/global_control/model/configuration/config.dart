/// Global network configuration model
@JsonSerializable()
class Config {
  Config(
    this.environments,
    this.gqlEnvironments,
    this.gqlMappingOverrides,
    this.headers,
    this.useMockNetworkClient,
    this.certificateType,
    this.otpServiceCode,
  );

  final String certificateType;
  final List<Environment> environments;
  final List<Environment> gqlEnvironments;
  final Map<String, String> gqlMappingOverrides;
  final Map<String, String> headers;
  final bool? useMockNetworkClient;
  final String? otpServiceCode;

  //Future<String> get certificate async => await rootBundle.loadString(certificatePath);
  List<int> get certificateBytes =>
      Certificate.getCertificateBytes(certificateType);

  factory Config.fromJson(Map<String, dynamic> json) => _$ConfigFromJson(json);

  Map<String, dynamic> toJson() => _$ConfigToJson(this);
}

/// Information pertaining to each environment.
@JsonSerializable()
class Environment {
  Environment(this.name, this.host, this.headers);

  final String name;
  final String host;

  final Map<String, String>? headers;

  factory Environment.fromJson(Map<String, dynamic> json) =>
      _$EnvironmentFromJson(json);

  Map<String, dynamic> toJson() => _$EnvironmentToJson(this);
}

